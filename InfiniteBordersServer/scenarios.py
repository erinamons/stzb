def get_all_cities():
    """获取所有城池的扁平列表 [(q, r, name, level, type, region), ...]"""
    all_cities = []
    for region, cities in FIXED_CITIES.items():
        for q, r, name, level, city_type in cities:
            all_cities.append((q, r, name, level, city_type, region))
    return all_cities


def get_all_gates():
    """获取所有关口的扁平列表 [(q, r, gate_name), ...]"""
    all_gates = []
    for (reg_a, reg_b), gates in FIXED_GATES.items():
        for q, r, name in gates:
            all_gates.append((q, r, name))
    return all_gates


def get_city_at(q, r):
    """查询指定坐标的城池信息，返回 (name, level, type, region) 或 None"""
    for region, cities in FIXED_CITIES.items():
        for cq, cr, name, level, city_type in cities:
            if cq == q and cr == r:
                return (name, level, city_type, region)
    return None
